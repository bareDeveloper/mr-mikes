/**
 * Get price for a specific region from pricing array
 */
function mrmikes_get_personalized_item_price($pricing_array, $region_name) {
    if (!$pricing_array || !is_array($pricing_array)) {
        return '';
    }

    foreach ($pricing_array as $price_row) {
        if (isset($price_row['region']) && is_object($price_row['region'])) {
            if ($price_row['region']->name === $region_name) {
                return $price_row['price'];
            }
        }
    }

    return '';
}

