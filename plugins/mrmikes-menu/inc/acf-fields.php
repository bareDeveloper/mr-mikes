/**
 * Add main Special Menus options page
 */
function mrmikes_add_special_menus_page() {
    // Debug: Check if ACF is available
    if (!function_exists('acf_add_options_page')) {
        error_log('MrMikes Debug: ACF acf_add_options_page function not available');

        // Show admin notice
        add_action('admin_notices', function() {
            echo '<div class="notice notice-error"><p><strong>MrMikes Menu:</strong> Advanced Custom Fields (ACF) plugin is required for the Special Menus feature.</p></div>';
        });
        return;
    }

    error_log('MrMikes Debug: Creating Special Menus main page');

    $main_page = acf_add_options_page(array(
        'page_title' => 'Special Menus',
        'menu_title' => 'Special Menus',
        'menu_slug' => 'special_menus',
        'capability' => 'edit_posts',
        'icon_url' => 'dashicons-food',
        'position' => 30, // Position in admin menu
    ));

    error_log('MrMikes Debug: Special Menus main page result: ' . print_r($main_page, true));
}

